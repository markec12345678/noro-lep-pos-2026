type HttpHandler = (
  req: IncomingMessage,
  res: ServerResponse,
  body: any,
) => Promise<void> | void;

const parseBody = (req: IncomingMessage): Promise<any> => {
  return new Promise((resolve, reject) => {
    let data = "";
    req.on("data", (chunk) => (data += chunk));
    req.on("end", () => {
      if (!data) return resolve({});
      try {
        resolve(JSON.parse(data));
      } catch {
        reject(new Error("Invalid JSON body"));
      }
    });
    req.on("error", reject);
  });
};

const sendJson = (res: ServerResponse, status: number, data: unknown) => {
  res.writeHead(status, {
    "Content-Type": "application/json",
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Methods": "GET, POST, OPTIONS",
    "Access-Control-Allow-Headers": "Content-Type, api-key",
  });
  res.end(JSON.stringify(data));
};

const handleHealth = async (_req: any, res: ServerResponse) => {
  sendJson(res, 200, {
    status: "ok",
    service: "furs-service",
    port: PORT,
    mode: config.mode,
    hasCert: Boolean(config.certPath && config.keyPath),
    timestamp: new Date().toISOString(),
  });
};

const handleGenerateZoi = async (
  _req: any,
  res: ServerResponse,
  body: any,
) => {
  const required = [
    "taxNumber",
    "issueDateTime",
    "invoiceNumber",
    "businessUnit",
    "electronicDevice",
  ];
  for (const field of required) {
    if (!body?.[field]) {
      return sendJson(res, 400, { error: `Missing required field: ${field}` });
    }
  }

  try {
    const { zoi, md5Hash } = computeZOI({
      taxNumber: body.taxNumber,
      issueDateTime: body.issueDateTime,
      invoiceNumber: body.invoiceNumber,
      businessUnit: body.businessUnit,
      electronicDevice: body.electronicDevice,
      controlSeq: body.controlSeq ?? 1,
    });

    // Sign the hash with the demo key — used in real submissions
    // to prove the ZOI was generated by the legitimate issuer.
    const signature = signHash(md5Hash, demoKeyPair.privateKey);

    const qrCode = await generateQrCode({
      zoi,
      issueDateTime: body.issueDateTime,
      invoiceNumber: body.invoiceNumber,
      taxNumber: body.taxNumber,
      total: body.total ?? 0,
    });

    return sendJson(res, 200, {
      zoi,
      qrCode,
      signatureLength: signature.length,
    });
  } catch (err) {
    console.error("[furs] ZOI generation failed:", err);
    return sendJson(res, 500, {
      error: "ZOI generation failed",
      detail: err instanceof Error ? err.message : "Unknown error",
    });
  }
};

const handleSubmitInvoice = async (
  _req: any,
  res: ServerResponse,
  body: any,
) => {
  if (!body?.zoi) {
    return sendJson(res, 400, { error: "Missing required field: zoi" });
  }

  try {
    const result = await submitInvoice(body);
    return sendJson(res, 200, result);
  } catch (err) {
    console.error("[furs] Invoice submission failed:", err);
    return sendJson(res, 500, {
      error: "Invoice submission failed",
      detail: err instanceof Error ? err.message : "Unknown error",
    });
  }
};

const routes: Record<string, Record<string, HttpHandler>> = {
  "/api/furs/health": { GET: handleHealth },
  "/api/furs/generate-zoi": { POST: handleGenerateZoi },
  "/api/furs/submit-invoice": { POST: handleSubmitInvoice },
};

const server = createServer(async (req, res) => {
  // Handle CORS preflight
  if (req.method === "OPTIONS") {
    res.writeHead(204, {
      "Access-Control-Allow-Origin": "*",
      "Access-Control-Allow-Methods": "GET, POST, OPTIONS",
      "Access-Control-Allow-Headers": "Content-Type, api-key",
    });
    return res.end();
  }

  const url = new URL(req.url ?? "/", `http://localhost:${PORT}`);
  const route = routes[url.pathname];
  if (!route) {
    return sendJson(res, 404, { error: "Not found", path: url.pathname });
  }

  const handler = route[req.method ?? "GET"];
  if (!handler) {
    return sendJson(res, 405, { error: "Method not allowed" });
  }

  try {
    const body = req.method === "POST" ? await parseBody(req) : {};
    await handler(req, res, body);
  } catch (err) {
    console.error("[furs] Request handler error:", err);
    sendJson(res, 500, {
      error: "Internal server error",
      detail: err instanceof Error ? err.message : "Unknown",
    });
  }
});

server.listen(PORT, () => {
  console.log(`[furs-service] Running on port ${PORT}`);
  console.log(`[furs-service] Mode: ${config.mode}`);
  console.log(
    `[furs-service] Cert configured: ${Boolean(config.certPath && config.keyPath)}`,
  );
  console.log(`[furs-service] Frontend calls via: /api/furs/*?XTransformPort=${PORT}`);
  if (config.mode === "test") {
    console.warn(
      "[furs-service] ⚠️  TEST MODE — EOR is mock-generated. Set FURS_MODE=production + FURS_CERT_PATH + FURS_KEY_PATH for real FURS submission.",
    );
  }
});

// Graceful shutdown
const shutdown = (signal: string) => {
  console.log(`[furs-service] Received ${signal}, shutting down...`);
  server.close(() => {
    console.log("[furs-service] Server closed");
    process.exit(0);
  });
};

process.on("SIGTERM", () => shutdown("SIGTERM"));
process.on("SIGINT", () => shutdown("SIGINT"));

// Catch unhandled errors so the server doesn't silently die
process.on("uncaughtException", (err: Error) => {
  console.error("[furs-service] UNCAUGHT EXCEPTION:", err);
});
process.on("unhandledRejection", (reason: unknown) => {
  console.error("[furs-service] UNHANDLED REJECTION:", reason);
});
