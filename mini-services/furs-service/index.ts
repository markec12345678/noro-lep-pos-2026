/**
 * Generate a QR code (as data URL) for the receipt per FURS format.
 *
 * QR content format (string):
 *   ZOI(unescaped) + issueDateTime + invoiceNumber + taxNumber + total
 *
 * Where:
 *   - ZOI: the 32-char protective mark
 *   - issueDateTime: YYYY-MM-DDTHH:MM:SS
 *   - invoiceNumber: e.g. "1"
 *   - taxNumber: 8 digits
 *   - total: invoice total with 2 decimal places (e.g. "12.50")
 *
 * Returns a PNG data URL suitable for embedding in <img src="...">.
 */
const generateQrCode = async (params: {
  zoi: string;
  issueDateTime: string;
  invoiceNumber: string;
  taxNumber: string;
  total: number;
}): Promise<string> => {
  // FURS QR content has a specific structure; the simplest accepted form
  // is the concatenation shown below. Real implementations should follow
  // the official FURS QR specification document.
  const content =
    params.zoi +
    params.issueDateTime +
    params.invoiceNumber +
    params.taxNumber +
    params.total.toFixed(2);

  const dataUrl = await QRCode.toDataURL(content, {
    errorCorrectionLevel: "M",
    margin: 1,
    width: 200,
  });
  return dataUrl;
};

