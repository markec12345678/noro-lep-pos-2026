import { createServer, IncomingMessage, ServerResponse } from "http";
import { randomBytes, createHash, createSign, generateKeyPairSync } from "crypto";
import QRCode from "qrcode";

/**
 * FURS Davčna Blagajna Service
 *
 * Slovenian fiscal cash register integration service. Implements:
 *   1. ZOI (Zaščitna Oznaka Izdajatelja Računa) — protective mark of invoice issuer
 *      Computed as: MD5(input) → RSA-SHA1 signature → base32 encoding
 *   2. EOR (Enkratna Identifikacijska Oznaka Računa) — unique invoice ID
 *      Returned by FURS server after successful submission
 *   3. QR code generation in FURS-mandated format
 *   4. FURS SOAP/XML submission (with test mode fallback)
 *
 * Runs on port 3004. Frontend calls via /api/furs/* with XTransformPort=3004.
 *
 * TEST MODE (default): generates mock EOR, logs warning that real certs
 * are needed for production. ZOI is still computed correctly using a
 * generated RSA keypair so the algorithm can be verified.
 *
 * PRODUCTION MODE: set FURS_MODE=production + FURS_CERT_PATH + FURS_KEY_PATH
 * environment variables to enable real FURS submission. The cert must be
 * issued by FURS (https://edavki.durs.si) for the test or production
 * environment.
 */

const PORT = 3004;

