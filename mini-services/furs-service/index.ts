/* ------------------------------------------------------------------ */
/* ZOI computation                                                     */
/* ------------------------------------------------------------------ */

/**
 * Compute ZOI per FURS specification.
 *
 * Input string format (concatenated, no separators):
 *   taxNumber + issueDateTime + invoiceNumber + businessUnit + electronicDevice + controlSeq
 *
 * Where:
 *   - taxNumber: 8-digit Slovenian tax number (davčna številka)
 *   - issueDateTime: YYYY-MM-DDTHH:MM:SS (e.g. "2024-06-27T15:30:00")
 *   - invoiceNumber: sequential, e.g. "1", "2", "3"...
 *   - businessUnit: FURS-issued business unit ID (e.g. "PRE")
 *   - electronicDevice: FURS-issued electronic device ID (e.g. "PRE1")
 *   - controlSeq: usually 1 (or "1" for the standard algorithm)
 *
 * Algorithm per FURS technical specification:
 *   1. Compute MD5 hash of the concatenated input string (16 bytes)
 *   2. ZOI = base32(MD5 hash) — yields 32 alphanumeric characters
 *
 * The MD5 hash is also signed with RSA-SHA1 (using the issuer's private
 * key) and the signature is sent to FURS in the SOAP envelope so they
 * can verify the ZOI was generated by the legitimate issuer. The ZOI
 * itself is just the base32 of the hash, NOT the signature.
 */
const computeZOI = (
  params: {
    taxNumber: string;
    issueDateTime: string;
    invoiceNumber: string;
    businessUnit: string;
    electronicDevice: string;
    controlSeq: number;
  },
): { zoi: string; md5Hash: Buffer } => {
  const input =
    params.taxNumber +
    params.issueDateTime +
    params.invoiceNumber +
    params.businessUnit +
    params.electronicDevice +
    String(params.controlSeq);

  // Step 1: MD5 hash of the concatenated input (16 bytes)
  const md5Hash = createHash("md5").update(input).digest();

  // Step 2: Base32-encode the MD5 hash (RFC 4648 alphabet, no padding)
  // 16 bytes → 26 chars (128 bits / 5 bits per char = 25.6, rounded up)
  const zoi = base32Encode(md5Hash);

  return { zoi, md5Hash };
};

/**
 * Sign the MD5 hash with RSA-SHA1 using the issuer's private key.
 * This signature is sent to FURS in the SOAP envelope for verification.
 * It is NOT part of the ZOI printed on the receipt.
 */
const signHash = (md5Hash: Buffer, privateKeyPem: string): Buffer => {
  const signer = createSign("SHA1");
  signer.update(md5Hash);
  signer.end();
  return signer.sign(privateKeyPem);
};

/**
 * Base32 encoding per RFC 4648 (no padding).
 * FURS uses this alphabet: ABCDEFGHIJKLMNOPQRSTUVWXYZ234567
 */
const base32Encode = (buffer: Buffer): string => {
  const alphabet = "ABCDEFGHIJKLMNOPQRSTUVWXYZ234567";
  let bits = 0;
  let value = 0;
  let output = "";

  for (const byte of buffer) {
    value = (value << 8) | byte;
    bits += 8;
    while (bits >= 5) {
      output += alphabet[(value >>> (bits - 5)) & 31];
      bits -= 5;
    }
  }
  if (bits > 0) {
    output += alphabet[(value << (5 - bits)) & 31];
  }
  return output;
};

