interface InvoiceSubmission {
  zoi: string;
  taxNumber: string;
  issueDateTime: string;
  invoiceNumber: string;
  businessUnit: string;
  electronicDevice: string;
  controlSeq: number;
  totalAmount: number;
  /** Tax breakdown by rate. */
  taxesByRate: Array<{ rate: number; base: number; tax: number; total: number }>;
  paymentMethod: "cash" | "card" | "other";
  /** Optional: customer's tax number (for B2B invoices). */
  customerTaxNumber?: string;
}

interface SubmissionResult {
  eor: string;
  submittedAt: string;
  mode: "test" | "production";
  /** Mark true when running in test mode without real FURS submission. */
  mock: boolean;
  /** Any FURS-returned messages or errors. */
  message?: string;
}

/**
 * Submit an invoice to FURS and obtain the EOR.
 *
 * In production mode, this constructs a SOAP envelope per the FURS
 * specification and POSTs it to the FURS endpoint using the configured
 * client certificate for TLS authentication.
 *
 * In test mode (default), it returns a deterministic mock EOR derived
 * from the ZOI so the receipt can still be printed with a unique-looking
 * identifier. Real submission must be done before going live.
 */
const submitInvoice = async (
  invoice: InvoiceSubmission,
): Promise<SubmissionResult> => {
  if (config.mode === "production" && config.certPath && config.keyPath) {
    try {
      const eor = await submitToFursReal(invoice);
      return {
        eor,
        submittedAt: new Date().toISOString(),
        mode: "production",
        mock: false,
      };
    } catch (err) {
      // In production, an error here means FURS is down or rejected the invoice.
      // The invoice must still be stored with ZOI for later offline submission.
      console.error("[furs] Real submission failed:", err);
      throw err;
    }
  }

  // TEST MODE — generate a mock EOR that looks realistic (32 hex chars)
  const mockEor = createHash("md5")
    .update(invoice.zoi + invoice.invoiceNumber + Date.now())
    .digest("hex")
    .toUpperCase()
    .slice(0, 36);

  return {
    eor: mockEor,
    submittedAt: new Date().toISOString(),
    mode: "test",
    mock: true,
    message:
      "TEST MODE: EOR is mock-generated. Configure FURS cert + FURS_MODE=production for real submission.",
  };
};

